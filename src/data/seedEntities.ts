import type { BusinessEntity, EntityType } from '@/types';

/**
 * Default demo directory of business entities. Fictional but realistic names,
 * using .example domains. The three "both" entities are shared customer +
 * supplier records — proof that one party is never duplicated across roles.
 *
 * Account link fields reference seed chart-of-accounts ids (e.g. seed_1221 =
 * Trade receivables) so future invoices/bills can post straight to the ledger.
 */
interface EntitySeedSpec {
  entityCode: string;
  legalName: string;
  tradingName?: string;
  entityType: EntityType;
  contactPerson: string;
  jobTitle: string;
  email: string;
  phone: string;
  mobile: string;
  website: string;
  country: string;
  city: string;
  addressLine1: string;
  postalCode: string;
  taxRegistrationNumber: string;
  crn: string;
  currency: string;
  bankName: string;
  customerCategory?: string;
  supplierCategory?: string;
  creditLimit?: number;
  withholdingTaxApplicable?: boolean;
  isActive?: boolean;
}

const TS = new Date('2024-01-01T00:00:00.000Z').toISOString();

// Chart-of-accounts links (seed account ids).
const AR = 'seed_1221'; // Trade receivables
const AP = 'seed_2210'; // Trade payables
const REVENUE = 'seed_4120'; // Service revenue
const EXPENSE = 'seed_6900'; // General administrative expenses

function fromSpec(spec: EntitySeedSpec, index: number): BusinessEntity {
  const isCustomer = spec.entityType === 'customer' || spec.entityType === 'both';
  const isSupplier = spec.entityType === 'supplier' || spec.entityType === 'both';
  return {
    id: `seedent_${spec.entityCode}`,
    entityCode: spec.entityCode,
    legalName: spec.legalName,
    tradingName: spec.tradingName ?? '',
    entityType: spec.entityType,
    contactPerson: spec.contactPerson,
    jobTitle: spec.jobTitle,
    email: spec.email,
    phone: spec.phone,
    mobile: spec.mobile,
    website: spec.website,
    country: spec.country,
    city: spec.city,
    addressLine1: spec.addressLine1,
    addressLine2: '',
    postalCode: spec.postalCode,
    taxRegistrationNumber: spec.taxRegistrationNumber,
    commercialRegistrationNumber: spec.crn,
    paymentTerms: 'NET_30',
    defaultCurrency: spec.currency,
    bankName: spec.bankName,
    bankAccountName: spec.legalName,
    iban: '',
    swiftCode: '',
    notes: '',
    isActive: spec.isActive ?? true,
    createdAt: TS,
    updatedAt: TS,

    customerCategory: isCustomer ? spec.customerCategory ?? 'retail' : '',
    creditLimit: isCustomer ? spec.creditLimit ?? 50000 : 0,
    defaultRevenueAccount: isCustomer ? REVENUE : '',
    defaultReceivableAccount: isCustomer ? AR : '',
    defaultInvoiceTemplateId: '',
    invoiceDeliveryMethod: isCustomer ? 'email' : '',
    customerPaymentTerms: isCustomer ? 'NET_30' : '',

    supplierCategory: isSupplier ? spec.supplierCategory ?? 'services' : '',
    defaultExpenseAccount: isSupplier ? EXPENSE : '',
    defaultPayableAccount: isSupplier ? AP : '',
    supplierPaymentTerms: isSupplier ? (index % 2 === 0 ? 'NET_45' : 'NET_30') : '',
    withholdingTaxApplicable: isSupplier ? spec.withholdingTaxApplicable ?? false : false,
    preferredPaymentMethod: isSupplier ? 'bank_transfer' : '',
  };
}

